
public class patientInformation {

	String name, address, insurance, phone, height, weight;
	
	public void SavePhone(String newPhone) {
		phone = newPhone;
	}
	
	public void SaveAddress(String newAddress) {
		address = newAddress;
	}
	
	public void SaveName(String newName) {
		name = newName;
	}
	
	public void SaveInsurance(String newInsurance) {
		insurance = newInsurance;
	}
	
	public void SaveHeight(String newHeight) {
		height = newHeight;
	}
	
	public void SaveWeight(String newWeight) {
		weight = newWeight;
	}
	
}
